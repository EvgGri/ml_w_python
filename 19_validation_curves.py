# недообучение. Проверочные кривые связаны с кривыми обучения, но только вместо того, чтобы график верностей на тренировочном и тестовом
# наборах как функциях от объема образца, мы варьируем значения модельных параметров, например параметр обратной регуляризации в логистической
# регрессии.
